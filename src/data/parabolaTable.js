export const getParabolaNorm = (overs, balls = 0) => {
    // Handle edge cases
    if (overs < 20) return 0; // Or handle as error/interpolation? Table starts at 20.
    if (overs > 50) return 225; // Cap at 50

    const row = parabolaTable[overs];
    if (!row) return 0;

    // balls should be 0-5
    if (balls < 0) balls = 0;
    if (balls > 5) balls = 5;

    return row[balls];
};
